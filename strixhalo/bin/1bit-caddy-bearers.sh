#!/usr/bin/env bash
# 1bit-caddy-bearers.sh — regenerate Caddy's bearer-token matcher snippet
# from /etc/caddy/bearers.txt.
#
# v1 had a single hardcoded bearer in /etc/caddy/Caddyfile. v2: each beta
# user has their own token; revoking one does not kick the other nine.
# 1bit-mesh-invite.sh + 1bit-mesh-revoke.sh append/delete lines in
# bearers.txt, then call this script to rebuild the shared matcher.
#
# Usage:
#   strixhalo/bin/1bit-caddy-bearers.sh           # regenerate snippet
#   strixhalo/bin/1bit-caddy-bearers.sh --reload  # regenerate + reload caddy
#
# Inputs:
#   /etc/caddy/bearers.txt — one entry per line, format:
#     sk-halo-<hex>  # <handle>  # issued <ISO>  # expires <ISO>
#   Comments (^#) and blank lines are ignored.
#
# Output:
#   /etc/caddy/bearers.conf — single named matcher consumed by the main
#   Caddyfile via `import bearer_matcher`:
#     (bearer_matcher) {
#         @authorized header_regexp Authorization ^Bearer\s+(sk-halo-aaa|sk-halo-bbb|...)$
#     }
#   Owned root:caddy 0640, atomic mv from tempfile.
#
# Empty-bearer behaviour: if bearers.txt has zero tokens, we emit a
# match that can never succeed (`^$`) so every route keeps its 401
# handler and nobody gets implicit-allow.
#
# Idempotent: if the would-be file matches the existing file byte-for-byte
# we skip the rename + reload.
#
# Exit codes:
#   0 success (or no-op)
#   2 usage
#   3 bearers.txt present but unreadable
#   4 caddy reload failed

set -euo pipefail

BEARER_FILE=${BEARER_FILE:-/etc/caddy/bearers.txt}
CONF_FILE=${CONF_FILE:-/etc/caddy/bearers.conf}
RELOAD=0

for arg in "$@"; do
    case "$arg" in
        --reload) RELOAD=1 ;;
        -h|--help)
            sed -n '2,30p' "$0"
            exit 0 ;;
        *)
            echo "usage: $0 [--reload]" >&2
            exit 2 ;;
    esac
done

# Collect tokens. We read via sudo because bearers.txt is root:caddy 0640
# and this script is intended to run as the invoking operator. When run
# as root (systemd/install hooks), sudo is a no-op.
SUDO=""
if [[ $EUID -ne 0 ]]; then
    SUDO="sudo"
fi

tokens=()
if [[ -e "$BEARER_FILE" ]]; then
    # readarray with process substitution; sudo cat is the only privileged read.
    while IFS= read -r line; do
        # Strip comments and trim whitespace.
        stripped="${line%%#*}"
        stripped="${stripped#"${stripped%%[![:space:]]*}"}"
        stripped="${stripped%"${stripped##*[![:space:]]}"}"
        [[ -z "$stripped" ]] && continue
        # Validate format: sk-halo- prefix + hex. Reject anything weird so a
        # stray wildcard in bearers.txt can't widen the regex to match all.
        if [[ ! "$stripped" =~ ^sk-halo-[A-Za-z0-9]+$ ]]; then
            echo "warn: skipping malformed token in $BEARER_FILE: '$stripped'" >&2
            continue
        fi
        tokens+=("$stripped")
    done < <($SUDO cat "$BEARER_FILE" 2>/dev/null || { echo "error: cannot read $BEARER_FILE" >&2; exit 3; })
fi

# Build the alternation. Empty set → `^$` (never matches a Bearer header).
if [[ ${#tokens[@]} -eq 0 ]]; then
    pattern='^$'
else
    # Tokens are already validated as [A-Za-z0-9-]; no regex escaping needed.
    joined="$(IFS='|'; echo "${tokens[*]}")"
    pattern="^Bearer\\s+(${joined})\$"
fi

# Render the snippet.
snippet="$(cat <<EOF
# Auto-generated by 1bit-caddy-bearers.sh — DO NOT EDIT BY HAND.
# Source: $BEARER_FILE
# Bearer count: ${#tokens[@]}
(bearer_matcher) {
    @authorized header_regexp Authorization ${pattern}
}
EOF
)"

# Idempotent write: only swap if the content actually changed.
existing=""
if [[ -e "$CONF_FILE" ]]; then
    existing="$($SUDO cat "$CONF_FILE" 2>/dev/null || true)"
fi

if [[ "$existing" == "$snippet" ]]; then
    echo "[caddy-bearers] $CONF_FILE already current (${#tokens[@]} tokens) — no change."
    exit 0
fi

tmp="$(mktemp)"
printf '%s\n' "$snippet" > "$tmp"
$SUDO install -o root -g caddy -m 0640 "$tmp" "$CONF_FILE"
rm -f "$tmp"
echo "[caddy-bearers] wrote $CONF_FILE (${#tokens[@]} tokens)."

if (( RELOAD )); then
    if ! $SUDO systemctl reload caddy.service; then
        echo "error: systemctl reload caddy.service failed" >&2
        exit 4
    fi
    echo "[caddy-bearers] reloaded caddy.service."
fi
